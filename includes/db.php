                $db = null;
        
                if ( $query->rowCount() == 1 ) {
                    
                    $result = $query->fetch();
                    return $result;
                    
                }
                
                return null;
        	    
    	    } catch( PDOException $e ) {
        	    
        	    $db = null;
        	    exit( 'Failed to get the LTI exercise.' );
        	    
    	    }
    	    
	    }
	    
