    	    $db = DB::getDB();
    	    
    	    try {
        	    
        	    $sql = 'SELECT e.exercise_id, e.video_src, e.markup_src, e.name, e.description, e.attempts, e.allow_retake, e.allow_new, ce.status status_id, e.exrs_type_id, e.category_id, ce.created_on ';
